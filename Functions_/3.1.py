# Write a program that calculates and prints:

# square root of 7
# natural logarithm of 5
# sine of 90 degrees

import math

square_root = math.sqrt(7)
print('A square root of 7 is', round(square_root,2))

nat_log = math.log(5)
print('Natural logarithm of 5', round(nat_log, 2))

sine = math.sin(90)
print('Sine of 90 degrees:', round(sine,2))